    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String CUSTOMER= jTextField1.getText();
        String PHONE= jTextField3.getText();
        
        try{
          
          Connection con= CONNECTION.getCon();
          Statement st= con.createStatement();
          st.executeQuery("DELETE FROM PELATES WHERE CUSTOMER='"+CUSTOMER+"' AND PHONE='"+PHONE+"' ");      
          JOptionPane.showMessageDialog(null, "Successfully updated");
          setVisible(false);
          new delete_readers().setVisible(true);
          st.close();
          con.close();
          
        }
        catch(Exception e){
          JOptionPane.showMessageDialog(null, "Customer already deleted");
          setVisible(false);
          new delete_readers().setVisible(true);
          
        }
    }//GEN-LAST:event_jButton1ActionPerformed

