    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try
        {
            Connection con= CONNECTION.getCon();
            Statement st= con.createStatement();
            ResultSet rs = st.executeQuery("select * from EKDHLWSEIS");
            DefaultTableModel tm = (DefaultTableModel)table3.getModel();
            tm.setRowCount(0);
            
        while(rs.next()){
        
            Object o[] = {rs.getString("EKD_ID"),rs.getString("BEGIN"),rs.getString("TIME"),rs.getString("EVENTNAME"),rs.getString("INVITED")};
            tm.addRow(o);
            
        
        }
            
        }catch(Exception e){
            
            JOptionPane.showMessageDialog(this, e);
            
        }
    }//GEN-LAST:event_jButton1ActionPerformed

